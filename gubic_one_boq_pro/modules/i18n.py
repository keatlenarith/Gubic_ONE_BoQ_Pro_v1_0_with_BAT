def get_language() -> str:
    lang = st.session_state.get("language", DEFAULT_LANGUAGE)
    return lang if lang in LANGUAGE_OPTIONS else DEFAULT_LANGUAGE


def set_language(lang: str) -> None:
    if lang in LANGUAGE_OPTIONS:
        st.session_state["language"] = lang


def t(key: str, **kwargs: Any) -> str:
    lang = get_language()
    value = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["EN"].get(key) or key
    if kwargs:
        try:
            return value.format(**kwargs)
        except Exception:
            return value
    return value


def language_selector(location: str = "sidebar") -> str:
    """Render a language selector and return active language."""
    current = get_language()
    keys = list(LANGUAGE_OPTIONS.keys())
    index = keys.index(current) if current in keys else 0
    label = f"{TRANSLATIONS['EN']['language']} / {TRANSLATIONS['KH']['language']}"
    help_text = TRANSLATIONS[current].get("language_hint", TRANSLATIONS["EN"]["language_hint"])

    widget_parent = st.sidebar if location == "sidebar" else st
    selected = widget_parent.selectbox(
        label,
        options=keys,
        index=index,
        format_func=lambda code: "English" if code == "EN" else "ខ្មែរ",
        key=f"language_selector_{location}",
        help=help_text,
    )
    set_language(selected)
    return selected
